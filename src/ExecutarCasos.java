
public class ExecutarCasos {

	public static void main(String[] args) {
		//Criando uma variável
		int dia;
		//Incrementando a varivel 
		dia = 1;
		
		//Montando uma estrutura de casos
		switch (dia) {
		case 1:
			System.out.println("Olá meu amigo hoje é Domingo");
			break;
			
		case 2:
			System.out.println("Jesus hoje é segunda-feira");
			break;
			
		case 3:
			System.out.println("Hoje é terça-feira");
			break;
		}

	}

}
